# Operator

num1 = int(input("Masukkan Angka 1 : "))
num2 = int(input("Masukkan Angka 2 : "))

# Operasi Penjumlahan
print("=============================================")
print("=            Operasi Penjumlahan            =")
print("=============================================")
penjumlahan = num1 + num2
print(num1,"+",num2,"=",penjumlahan)

# Operasi Pengurangan
print("\n=============================================")
print("=            Operasi Pengurangan            =")
print("=============================================")
pengurangan = num1 - num2
print(num1,"-",num2,"=",pengurangan)

# Operasi Perkalian
print("\n=============================================")
print("=            Operasi Perkalian              =")
print("=============================================")
perkalian = num1 * num2
print(num1,"*",num2,"=",perkalian)

# Operasi Pembagian
print("\n=============================================")
print("=            Operasi Pembagian              =")
print("=============================================")
pembagian = num1 / num2
print(num1,"/",num2,"=",pembagian)

# Operasi Sisa Bagi/Modulus
print("\n=============================================")
print("=            Operasi Modulus                =")
print("=============================================")
modulus = num1 % num2
print(num1,"%",num2,"=",modulus)

# Operasi Pangkat
print("\n=============================================")
print("=            Operasi Pemangkatan            =")
print("=============================================")
pemangkatan = num1 ** num2
print(num1,"**",num2,"=",pemangkatan)